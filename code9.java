class code9 {
    public static void main(String[] args) {
        int a = 3;   // hardcoded input
        int b = 4;
        int c = 5;

        if ((a * a + b * b) == (c * c)) {
            System.out.println("It is a Pythagorean triplet");
        } else {
            System.out.println("It is not a Pythagorean triplet");
        }
    }
}
